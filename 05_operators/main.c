#include <stdio.h>

int main(void)
{

    int number1 = 4;
    int number2 = 5;

    printf(" Sum : %d  \n", number1 + number2);
    printf(" Subtraction : %d \n", number1 - number2);
    printf(" Multiplication : %d \n" ,number1 * number2);
    printf(" Division : %d  \n", number1 / number2);


    float number3 = 3.7;
    float number4 = 7.9;

    printf(" Sum : %f \n", number3 + number4);
    printf(" Subtraction : %f  \n", number3 - number4);
    printf(" Multiplication : %f \n" ,number3 * number4);
    printf(" Division : %f  \n", number3 / number4);

    return 0;
}

